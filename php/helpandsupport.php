<h2>Recovering your customer ID.</h2>
Forgotten your customer ID? Don’t worry, it happens to the best of us.You can find your customer ID in the email you were sent when you opened your account. 
(Just in case you need to search your inbox, the email subject line is ‘Your account has been opened’.)If you can’t find your welcome letter or email, we can give you your customer
 ID over the phone. Call us on 13 00 00, Monday to Friday 8am–8pm or Saturday 9am–5pm (AEST).
</br>
<h2>Cancelling cards for suspected fraud</h2>
If we detect that your card has been used at an ATM or payment device that is suspected of being connected with fraudulent activity, we’re obliged to cancel the card immediately – to protect you and your money. 

If this happens to your card, we’ll send you a text to let you know what’s happened, and we’ll ask you to get in touch. Just give us a call on 13 15 63 and we’ll get a new card sent out to you.

If you need to cancel or stop a card yourself, check out our cancelling a card help article.</br>

<h2>Redrawing from your home loan</h2>

If you’re ahead on your repayments, most of ME’s loans let you take money out of your loan to use for something else – useful if you find yourself short on funds, or you just want to make a big purchase.

Of course, withdrawing funds back out of your loan adds to the amount left owing, which may also increase your minimum repayment amount.

